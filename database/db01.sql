create table DOCUMENT
(
    ID      NUMBER(20, 0) GENERATED BY DEFAULT AS IDENTITY MINVALUE 1 MAXVALUE 999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER NOCYCLE NOKEEP NOSCALE NOT NULL,
    NAME    VARCHAR2(50 CHAR) NOT NULL,
    ARCHIVE BLOB              NOT NULL,
    CONTENT CLOB              NOT NULL
);

